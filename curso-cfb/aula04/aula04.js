// OPERADORES MATEMÁTICOS
let num1 = 0, num2 = 0; // em uma linha podemos declarar várias variáveis;
// num1 = num2 = num3 = 10

num1 = 5
num2 = 10

res = num1 + num2 // soma (+)
res = num1 - num2 // subtração (-)
res = num1 * num2 // multiplicação (*)
res = num1 / num2 // divisão (/)
res = num1 % num2 // resto da divisão (%) > mod
res = num1 ** num2 // exponenciação

num1++ // incremento (adiciona +1 a variável)
num1-- // decremento (reduz -1 a variável)

num2 += 1 // num2 = num2 + 1
num2 -= 1 // num2 = num2 - 1
num2 *= 1 // num2 = num2 * 1
num2 /= 1 // num2 = num2 / 1
num2 %= 1 // num2 = num2 % 1

// temos a precedência -> (), **, * / %, + -

console.log(res);
