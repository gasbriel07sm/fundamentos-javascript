/**
 * Operadores de Comparação
 * > maior que
 * < menor que
 * >= maior ou igual
 * <= menor ou igual
 * == igual
 * === estritamente igual
 * != diferente
 * !== estritamente diferente
 * 
 * os estritamentes verificam os tipos dos dados também , os outros checam apenas valor
 * sempre retornar um valor booleano (true ou false)
*/

console.log(10 > 5);
console.log(10 < 5);
console.log(10 >= 5);
console.log(10 <= 5);

// os estritos verificam o tipo do dado também, não apenas o valor
console.log(10 == '10')
console.log(10 === '10')

console.log('10' != 10)
console.log('10' !== 10)


